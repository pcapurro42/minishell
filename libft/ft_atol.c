int	ft_atol(const char *s)
{
	int			i;
	long int	result;
	int			sign;

	i = 0;
	result = 0;
	sign = 1;
	while ((s[i] >= 9) && ((s[i] <= 13) || s[i] == 32))
		i++;
	if (s[i] == 45 || s[i] == 43)
	{
		if (s[i] == '-')
			sign = sign * (-1);
		i++;
	}
	while (s[i] >= 48 && s[i] <= 57)
	{
		result = (result * 10) + (s[i] - 48);
		i++;
	}
	return (result);
}
