#include "libft.h"

int	ft_isnum(char *str)
{
	if (!str || str[0] == '\0')
		return (0);
	if ((str[0] == '-' && str[1] != '\0') || (str[0] == '+' && str[1] != '\0'))
		str++;
	while (*str != '\0')
	{
		if (!(48 <= *str && *str <= 57))
			return (0);
		str++;
	}
	return (1);
}
